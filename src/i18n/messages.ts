function withBase(overrides: MessageOverrides): AppMessages {
  return {
    ...baseZhCN,
    ...overrides,
    app: { ...baseZhCN.app, ...overrides.app },
    common: { ...baseZhCN.common, ...overrides.common },
    language: { ...baseZhCN.language, ...overrides.language },
    sider: { ...baseZhCN.sider, ...overrides.sider },
    templateManager: { ...baseZhCN.templateManager, ...overrides.templateManager },
    header: { ...baseZhCN.header, ...overrides.header },
    feedback: { ...baseZhCN.feedback, ...overrides.feedback },
    editor: { ...baseZhCN.editor, ...overrides.editor },
    preview: { ...baseZhCN.preview, ...overrides.preview },
    settings: {
      ...baseZhCN.settings,
      ...overrides.settings,
      fields: { ...baseZhCN.settings.fields, ...overrides.settings?.fields },
      backgroundOptions: {
        ...baseZhCN.settings.backgroundOptions,
        ...overrides.settings?.backgroundOptions,
      },
      fontOptions: { ...baseZhCN.settings.fontOptions, ...overrides.settings?.fontOptions },
      layoutOptions: { ...baseZhCN.settings.layoutOptions, ...overrides.settings?.layoutOptions },
      curveOptions: { ...baseZhCN.settings.curveOptions, ...overrides.settings?.curveOptions },
    },
    markdownImport: { ...baseZhCN.markdownImport, ...overrides.markdownImport },
    status: { ...baseZhCN.status, ...overrides.status },
    render: { ...baseZhCN.render, ...overrides.render },
    syntaxAssistant: { ...baseZhCN.syntaxAssistant, ...overrides.syntaxAssistant },
    templates: overrides.templates ?? baseZhCN.templates,
    diagnostics: {
      ...baseZhCN.diagnostics,
      ...overrides.diagnostics,
      diagramLabels: {
        ...baseZhCN.diagnostics.diagramLabels,
        ...overrides.diagnostics?.diagramLabels,
      },
      rules: { ...baseZhCN.diagnostics.rules, ...overrides.diagnostics?.rules },
    },
  };
}

